export const CATEGORIES: QuestionCategory[] = [
  'personal',
  'wouldyou',
  'hottakes',
  'hypotheticals',
  'community',
  'blend',
];

export function getRandomQuestion(
  category: QuestionCategory,
  usedQuestions: string[] = []
): string {
  let availableQuestions: string[] = [];

  if (category === 'personal') {
    availableQuestions = personalQuestions;
  } else if (category === 'wouldyou') {
    availableQuestions = wouldYouQuestions;
  } else if (category === 'hottakes') {
    availableQuestions = hotTakesQuestions;
  } else if (category === 'hypotheticals') {
    availableQuestions = hypotheticalQuestions;
  } else if (category === 'community') {
    availableQuestions = communityQuestions;
  } else if (category === 'blend') {
    // Combine all questions for blend mode
    availableQuestions = [
      ...personalQuestions,
      ...wouldYouQuestions,
      ...hotTakesQuestions,
      ...hypotheticalQuestions,
      ...communityQuestions,
    ];
  }

  // Filter out used questions
  const remainingQuestions = availableQuestions.filter(
    (q) => !usedQuestions.includes(q)
  );

  if (remainingQuestions.length === 0) {
    // If all questions have been used, return a random one from the full pool
    return availableQuestions[
      Math.floor(Math.random() * availableQuestions.length)
    ];
  }

  // Return a random question from remaining questions
  return remainingQuestions[Math.floor(Math.random() * remainingQuestions.length)];
}
