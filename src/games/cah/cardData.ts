// Helper functions
export function shuffleArray<T>(array: T[]): T[] {
  const shuffled = [...array];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}

export function getRandomBlackCard(): BlackCard {
  return blackCards[Math.floor(Math.random() * blackCards.length)];
}

export function dealWhiteCards(count: number, exclude: string[] = []): WhiteCard[] {
  const available = whiteCards.filter(c => !exclude.includes(c.id));
  return shuffleArray(available).slice(0, count);
}
